ipv4_regex = /^([1-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])$/

ip = ARGV[0] ? ARGV[0] : "192.168.0.1"
if ip.match?(ipv4_regex)
  puts "Valid IPv4 address"
else
  puts "Not a valid IPv4 address"
end

